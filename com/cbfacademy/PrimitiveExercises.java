package com.cbfacademy;

public class PrimitiveExercises {
    public static void main(String[] args) {

        // Arithmetic Operations
        double num1 = 2.34;
        double num2 = 7.3;

        System.out.println(num2 + num1);
        System.out.println(num2 - num1);
        System.out.println(num2 * num1);
        System.out.println(num2 / num1);

        // Relational Operations
        double num3 = 2.34;
        double num4 = 7.3;

        System.out.println(num3 == num4);
        System.out.println(num3 != num4);
        System.out.println(num3 > num4);
        System.out.println(num3 > num4);

    }
}
